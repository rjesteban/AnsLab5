    private void SetStemUnitButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SetStemUnitButton2ActionPerformed
        // TODO add your handling code here:
        stem = Double.valueOf(SetStemUnitField2.getText());
        if(stem.compareTo(leaf)==1){
            SetStemUnitButton2.setEnabled(false);
            SetStemUnitField2.setEnabled(false);
        }
        else{
            stem = null;
            JOptionPane.showMessageDialog(null, "Stem should be greater than the leaf");
        }
    }//GEN-LAST:event_SetStemUnitButton2ActionPerformed

