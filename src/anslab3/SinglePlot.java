    private void SetRightValuesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SetRightValuesButtonActionPerformed
        // TODO add your handling code here:
        datainput = SetRightValuesField.getText();
        inputs = salg.convertToArray(datainput);
        location1 = new LinkedList<Double>(inputs);
        Collections.sort(location1);
        
        ValuesSet.setText(Arrays.toString(location1.toArray()));
        Size.setText(String.valueOf(location1.size()));
        
        SetRightValuesButton.setEnabled(false);
        SetRightValuesField.setEnabled(false);
        
        
        inputs.clear();
    }//GEN-LAST:event_SetRightValuesButtonActionPerformed

